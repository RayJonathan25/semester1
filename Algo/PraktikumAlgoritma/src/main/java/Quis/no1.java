package Quis;

public class no1 {

  public static void main(String[] args) {
    double a, b, c = 0;
    a = 3;
    b = 4;
    c = Math.sqrt((a * a) + (b * b));
    System.out.println("Hasil dari rumus Pythagoras adalah = " + c);

    double phi, jari, volume, tinggi;
    phi = 3.14;
    jari = 3;
    tinggi = 6;
    volume = 0;
    volume = 0.33 * phi * (jari * jari) * tinggi;
    System.out.println("Hasil dari rumus Volume Kerucut adalah = " + volume);
  }
}
